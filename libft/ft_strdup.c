#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*cpy;

	if (s == NULL)
		return (NULL);
	i = ft_strlen(s);
	cpy = malloc(sizeof(char) * (i + 1));
	if (cpy == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
